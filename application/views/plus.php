L’exemple désolant de la Tasmanie, région de l’Australie grande comme l’Irlande, est révélateur. Les forêts primaires de Tasmanie, notamment les forêts d’eucalyptus, uniques au monde, sont détruites méthodiquement par des sociétés privées … et publiques. Pourquoi ? Tout simplement pour replanter 1 seule espèce d’arbre destiné à faire de la pâte à papier.

C’est toute l’incroyable biodiversité des forêts humides, fruits d’un équilibre écologique multimillénaire, qui est menacée. 20% de l’île sont déjà occupés par les plantations qui sont protégés par leurs exploitants en empoisonnant les animaux sauvages. En effet, pour empêcher les animaux, privés de leur habitat naturel, de venir se nourrir des jeunes plants ; les forestiers utilisent un agent toxique pour les empoisonner.

La moitié est due à des coupes illégales. Les conséquences ne se font pas sentir que sur l'effet de serre mais également localement sur les sols qui ruissellent plus et donc favorisent les inondations, l'évaporation et donc la sécheresse. Nouvelle estimation de la déforestation en 2014

La FAO a procédé  une nouvelle évaluation des changements intervenus dans les forêts de la planète: cette étude met en évidence un recul des utilisations de terres forestières entre 1990 et 2010.

A savoir :  les forêts du monde sont réparties ainsi : forêts tropicales (45% du total), boréales (31%), tempérées (16%) et subtropicales (8%).

« Si nous voulons sérieusement mettre fin à la déforestation, conformément à l’objectif de la FAO « Zéro déforestation illégale », il faudra impérativement partir d’informations et de données fiables », a déclaré le directeur général de la FAO, José Graziano da Silva.

