        <h2>Les besoins des plantes </h2>
        <p class="separator">Les besoins d'une plante
Une plante est un être vivant.
Une plante respire et se nourrit comme un être humain ! Elle se développe et grandit.
Certaines donnent des fleurs et des fruits. Au bout d'un certain temps, la plante meurt.
Comme toi, les plantes ont besoin d'eau pour vivre. Mais ce n'est pas tout !
Les plantes ont besoin de trouver de l'énergie pour se développer, fleurir ou encore donner
des fruits.
Pour franchir ces étapes, les plantes vont utiliser l'énergie du soleil ! Ce processus très
particulier s'appelle la photosynthèse .</p>

      </div>
      <div class="section-title text-center">

        <h2>Profiter de l'énergie solaire </h2>
        <p class="separator">.La photosynthèse est un ensemble de réactions chimiques qui utilisent les rayons
ultraviolets du soleil et le dioxyde de carbone (CO2) contenu dans l'air pour fournir des
sucres à la plante.
Ce que la photosynthèse apporte à la plante, c'est à peu près ce que t'apporte la digestion
!
La digestion transforme l'énergie contenue dans ce que tu manges en éléments directement
utilisables par ton corps.
La photosynthèse transforme l'énergie solaire et le CO2 en éléments utilisables par la
plante pour sa croissance.
Les réactions chimiques de la photosynthèse produisent du dioxygène (O2).
La photosynthèse produit de l'oxygène!
L'air que tu respires est composé de diazote à 78%, de dioxygène à 21% (ce gaz est
souvent appelé oxygène) et d'autres gaz à 1% dont 0.03% de dioxyde de carbone (CO2)
Lorsque tu respires, tu puises du dioxygène dans l'air et tu rejettes du CO2 !
L'air que tu inspires ne contient que 0.03% de CO2 et 21% de dioxygène alors que celui
que tu expires contient 5% de CO2 et 16% de dioxygène.
</p>
<p>La présence de dioxygène dans l'air est donc essentielle à la survie des Hommes comme à
celles de nombreuses espèces animales.
La photosynthèse joue donc un rôle très important : grâce à elles, les plantes consomment
du CO2 et produisent de l'oxygène nécessaire à la vie !
C'est parce que les grandes forêts tropicales et les océans sont les principaux lieux où se
produit la photosynthèse qu'ils sont importants pour l'équilibre de la planète !
Non seulement ces milieux naturels servent de refuge à de nombreuses espèces animales
et végétales, mais ils permettent de combattre en partie l'effet de serre en limitant la
quantité de CO2 dans l'atmosphère.</p>

