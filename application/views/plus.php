Il existe des différences énormes  entre les régions, Ainsi, « les forêts tropicales ont diminué en Amérique du Sud, en Afrique et en Asie, l’Amérique du Sud tropicale accusant la plus grosse perte en termes absolus, suivie de l’Afrique tropicale, tandis que l’Asie subtropicale et tempérée a affiché des gains de superficies boisées ».</p>

